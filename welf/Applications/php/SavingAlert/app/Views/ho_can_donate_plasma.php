                <div class="grid-text" data-mh="grid-text">
                    <h2>Exclusions</h2>
                    <p>Some factors may exclude individuals from donating plasma:</p>
                    <ul>
                        <li>Recent travel to certain countries with endemic diseases.</li>
                        <li>Recent tattoos or body piercings (typically within the last 4-6 months).</li>
                        <li>Pregnancy or breastfeeding.</li>
                        <li>History of certain medical conditions or treatments.</li>
                        <li>Current use of certain medications or drugs.</li>
                    </ul>
                    <p>It's important to check with your local plasma donation center for specific eligibility requirements and exclusions.</p>
                </div>

                                               
<div class="grid-text" data-mh="grid-text" style="">
