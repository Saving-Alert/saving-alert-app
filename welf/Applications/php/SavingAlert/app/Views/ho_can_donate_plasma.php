                <div class="grid-text" data-mh="grid-text">
                    <h2>How to Donate Plasma</h2>
                    <p>The process of donating plasma is similar to donating blood:</p>
                    <ul>
                        <li>Schedule an appointment at a plasma donation center.</li>
                        <li>Complete a medical questionnaire and screening.</li>
                        <li>Undergo the donation process, which typically takes about 1-2 hours.</li>
                        <li>Rest and hydrate after donation, and consider donating regularly if eligible.</li>
                    </ul> <br>
                    <p>Many donation centers provide detailed information and support throughout the donation process.</p>
