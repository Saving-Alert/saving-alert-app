                    <h2>Eligibility Criteria</h2>
                    <p>To donate plasma, individuals must meet certain eligibility criteria:</p>
                    <ul>
                        <li>Be in good health and feeling well on the day of donation.</li>
                        <li>Meet age requirements (usually 18-65 years old, but this can vary by donation center).</li>
                        <li>Weigh at least 50 kilograms (110 pounds).</li>
                        <li>Pass a brief medical screening to ensure safety for both the donor and the recipient.</li>
                        <li>Not have certain medical conditions or infections (determined during the screening).</li>
                    </ul>
                </div>



                                
